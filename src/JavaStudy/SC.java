package JavaStudy;
 class SC {
	 static final double PI = 3.1415;
	 
	 static double add(double n1, double n2) {
		 return n1 + n2;
	 }
	 static double min(double n1, double n2) {
		 return n1 - n2;
	 }
	 static double calCircleArea(double r) {
		 return PI * r * r;
	 }
	 static double calCirclePeri(double r) {
		 return PI * (r * 2);
	 }
 }